
public class Calculator {
    // Global variable that refers to the version of the program
    String version = "1.0.0";

    // Method that takes two integers and adds num1 and num2 together
    public int add(int num1, int num2) {
        return num1+num2;
    }
    // Method that takes two integers and subtracts num1 from num2
    public int subtract(int num1, int num2) {
        return num1-num2;
    }
    // Method that takes two integers and multiplies num1 and num2
    public int multiply(int num1, int num2) {
        return num1*num2;
    }
}